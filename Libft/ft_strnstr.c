#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	i;
	size_t	j;
	size_t	len_ndl;

	i = 0;
	len_ndl = ft_strlen(needle);
	if (needle[0] == '\0')
	{
		return ((char *)haystack);
	}
	while (haystack[i] && i <= (len - len_ndl))
	{
		j = 0;
		while (needle[j] == haystack[i + j] && i + j < len)
		{
			if (needle[j + 1] == '\0')
			{
				return ((char *)haystack + i);
			}
			j++;
		}
		i++;
	}
	return (0);
}
